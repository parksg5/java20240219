package ch04.lecture.p4for;

public class C04ForExercise {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        for (int i = 0; i < 5; i++) {
            System.out.println(2 * i);
        }
        for (int i = 0; i <= 5; i++) {
            System.out.println(2 * i);
        }
        for (int i = 4; i >= 0; i--) {
            System.out.println(i);
        }
        for (int i = 5; i > 0; i--) {
            System.out.println(i);
        }
    }
}
